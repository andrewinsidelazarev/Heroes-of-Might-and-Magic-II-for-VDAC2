; ---------------------------------------------------------------------
; RawPak_* — self-contained FAT32 reader over direct SD CMD18.
;
; This path intentionally bypasses the bundled WC CORE32 LOAD512 state. It
; supports both contiguous and fragmented files by following the FAT chain
; between every cluster. Current host image is superfloppy FAT32 with
; 512-byte sectors and spc=1; we validate that in RawPak_OpenRoot.
;
; Directory/file lookup matches by NAME, case-insensitive: it reconstructs the
; LONG name (LFN) when present, else the 8.3 short name, and compares to these
; targets. This is robust to whatever short alias the injector assigns (the
; observed bug was injector writing GAMES~1 while we searched "GAMES      ").

RAWPAK_NAME_PAK:
                DEFB "ZUMALVL.PAK", 0
RAWPAK_NAME_AUDIO:
                DEFB "ZUMAAUD.PAK", 0
RAWPAK_NAME_SOUND:
                DEFB "ZUMASND.PAK", 0
RAWPAK_NAME_MAIN:
                DEFB "ZUMAMAIN.PAK", 0
RAWPAK_NAME_BOOT:                               ; HOBETA WC loader в корне SD (для Quit)
                DEFB "BOOT.$C", 0
RAWPAK_BOOT_SIZE EQU 31761

; DBG instrumentation (read in F12 dump to bisect a failed open; addresses are
; resident in loader_resident.asm — ZiFi_DbgGamesA #6432, GpDbgStep #642D):
;   ZiFi_DbgGamesA = RawPak open granular step:
;       #20 entry, #21 sector0 read OK, #22 BPB valid, #26 search start, #25 PAK found;
;       #A1 sector0 CMD err, #A2 BPB invalid (bps!=512 / no FAT32 partition),
;       #A3 spc=0, #A6 PAK (name+size) not found anywhere, #A7 run-table overflow.
;   ZiFi_DbgGamesFound = directories visited during the recursive search.
RawPak_OpenRoot:
                LD   A, #20
                LD   (ZiFi_DbgGamesA), A
                XOR  A
                LD   (ZiFi_DbgGamesFound), A
                ; Invalidate the FAT-sector cache (FatBufLba = #FFFFFFFF). The cache
                ; in RawPak_FatNext skips re-reading a FAT sector already in FatBuf;
                ; the FAT is read-only for us, so it stays valid for the whole session.
                LD   HL, #FFFF
                LD   (RawPak_FatBufLba + 0), HL
                LD   (RawPak_FatBufLba + 2), HL
                ; --- Read sector 0 (boot sector OR MBR). LBA 0 is addressing-
                ; agnostic (arg 0 either way), so this read works before we know
                ; block vs byte. Then mirror ChkDsk: scan the MBR partition table;
                ; a real card is MBR-partitioned + SDHC (block-addressed), Unreal is
                ; a superfloppy (no table) + byte-addressed.
                LD   HL, 0
                LD   DE, 0
                CALL RawPak_ReadSectorBuffer
                JP   C, .ErrBpbRead
                LD   A, #21
                LD   (ZiFi_DbgGamesA), A
                XOR  A                          ; part_lba = 0 (superfloppy: sector 0 is the BPB)
                LD   (RawPak_PartLba + 0), A
                LD   (RawPak_PartLba + 1), A
                LD   (RawPak_PartLba + 2), A
                LD   (RawPak_PartLba + 3), A
                ; Is sector 0 itself a FAT32 BPB (bytes/sector == 512)? Then this is
                ; a superfloppy (Unreal) -> use sector 0 as BPB, byte addressing.
                ; Otherwise it is an MBR -> scan the partition table. (Checking the
                ; BPB first avoids false-matching boot-code bytes at offset 446.)
                LD   IX, #8000
                LD   A, (IX + 11)
                OR   A
                JR   NZ, .tryMbr
                LD   A, (IX + 12)
                CP   2
                JR   Z, .bpbReady               ; sector 0 is a 512-B/sector BPB -> superfloppy
.tryMbr:       LD   IX, #8000 + 446            ; MBR partition table: 4 entries x 16 B
                LD   B, 4
.mbrScan:      LD   A, (IX + 4)                 ; partition type
                CP   #0B                         ; FAT32 (CHS)
                JR   Z, .havePart
                CP   #0C                         ; FAT32 (LBA)
                JR   Z, .havePart
                LD   DE, 16
                ADD  IX, DE
                DJNZ .mbrScan
                JP   .ErrBps                    ; not a BPB and no FAT32 partition entry
.havePart:     LD   A, (IX + 8)  : LD (RawPak_PartLba + 0), A   ; partition start LBA
                LD   A, (IX + 9)  : LD (RawPak_PartLba + 1), A
                LD   A, (IX + 10) : LD (RawPak_PartLba + 2), A
                LD   A, (IX + 11) : LD (RawPak_PartLba + 3), A
                ; Найден MBR-раздел => это реальная SDHC-карта => блочная адресация
                ; АВТОРИТЕТНА. НЕ откатываемся на байтовую: байтовый режим на SDHC шлёт
                ; неверные адреса блоков и может вывести карту из строя (восстановление
                ; только физическим переткиванием). Откат на byte допустим лишь для
                ; настоящего superfloppy (там ветка .bpbReady достигается напрямую с
                ; sd_blkt=0). Раньше тут был откат на byte — он и кирпичил 32 ГБ карту.
                LD   A, 1
                LD   (sd_blkt), A
                CALL RawPak_ReadPartBpb
                JP   C, .ErrBps
.bpbReady:     LD   IX, #8000
                ; Require 512-byte sectors and a non-zero sectors/cluster value.
                LD   A, (IX + 11)
                OR   A
                JP   NZ, .ErrBps
                LD   A, (IX + 12)
                CP   2
                JP   NZ, .ErrBps
                LD   A, (IX + 13)
                OR   A
                JP   Z, .ErrSpc
                LD   (RawPak_Spc), A
                LD   A, #22
                LD   (ZiFi_DbgGamesA), A
                ; fatstart = part_lba + reserved sectors (16-bit in BPB)
                LD   HL, RawPak_PartLba
                LD   DE, RawPak_FatStart
                CALL RawPak_Copy32
                LD   A, (IX + 14) : LD (RawPak_Tmp + 0), A
                LD   A, (IX + 15) : LD (RawPak_Tmp + 1), A
                XOR  A : LD (RawPak_Tmp + 2), A : LD (RawPak_Tmp + 3), A
                LD   HL, RawPak_Tmp
                LD   DE, RawPak_FatStart
                CALL RawPak_Add32
                ; datastart = fatstart + numFATs * FATSz32
                LD   HL, RawPak_FatStart
                LD   DE, RawPak_DataStart
                CALL RawPak_Copy32
                LD   A, (IX + 16)
                LD   B, A
.fatLoop:      PUSH BC
                LD   A, (IX + 36) : LD (RawPak_Tmp + 0), A
                LD   A, (IX + 37) : LD (RawPak_Tmp + 1), A
                LD   A, (IX + 38) : LD (RawPak_Tmp + 2), A
                LD   A, (IX + 39) : LD (RawPak_Tmp + 3), A
                LD   HL, RawPak_Tmp
                LD   DE, RawPak_DataStart
                CALL RawPak_Add32
                POP  BC
                DJNZ .fatLoop
                ; root cluster
                LD   A, (IX + 44) : LD (RawPak_RootClus + 0), A
                LD   A, (IX + 45) : LD (RawPak_RootClus + 1), A
                LD   A, (IX + 46) : LD (RawPak_RootClus + 2), A
                LD   A, (IX + 47) : LD (RawPak_RootClus + 3), A
                ; --- граница тома для защиты SD: sd_lba_max = PartLba + BPB_TotSec32.
                ; Любой LBA >= границы отбивается в sd_read_sector (CF=1), не уходя в
                ; карту как CMD17. 0 = выключено (если TotSec32 почему-то нулевой).
                XOR  A
                LD   (sd_lba_max + 0), A
                LD   (sd_lba_max + 1), A
                LD   (sd_lba_max + 2), A
                LD   (sd_lba_max + 3), A
                LD   A, (IX + 32) : LD (RawPak_Tmp + 0), A : LD C, A
                LD   A, (IX + 33) : LD (RawPak_Tmp + 1), A : OR C : LD C, A
                LD   A, (IX + 34) : LD (RawPak_Tmp + 2), A : OR C : LD C, A
                LD   A, (IX + 35) : LD (RawPak_Tmp + 3), A : OR C
                JR   Z, .noVolGuard                     ; TotSec32==0 -> защиту не включаем
                LD   HL, RawPak_PartLba
                LD   DE, sd_lba_max
                CALL RawPak_Copy32                      ; sd_lba_max = PartLba
                LD   HL, RawPak_Tmp
                LD   DE, sd_lba_max
                CALL RawPak_Add32                       ; sd_lba_max += TotSec32
.noVolGuard:
                ; --- locate ZUMALVL.PAK. Fast path: reuse WC's active-panel
                ; path from page #F7, then fall back to a whole-card DFS.
                LD   A, #26
                LD   (ZiFi_DbgGamesA), A
                LD   A, (RawPak_CachedValid)   ; reuse the location found earlier this session
                OR   A
                JR   Z, .search
                LD   HL, RawPak_CachedPakClus
                LD   DE, RawPak_FileStartClus
                CALL RawPak_Copy32
                JR   .haveFile
.search:       CALL RawPak_TryWcPath
                JR   C, .cacheFound
                IF RAWPAK_WC_PATH_ONLY
                JP   .ErrPak
                ENDIF
                LD   HL, RawPak_RootClus
                LD   DE, RawPak_CurClus
                CALL RawPak_Copy32
                CALL RawPak_FindPakRecursive   ; DFS whole tree -> RawPak_FileStartClus
                JP   NC, .ErrPak
.cacheFound:
                LD   HL, RawPak_FileStartClus  ; cache for subsequent opens this session
                LD   DE, RawPak_CachedPakClus
                CALL RawPak_Copy32
                LD   A, 1
                LD   (RawPak_CachedValid), A
                LD   (LevelsMapLoaded), A      ; resident flag: skip "LOADING LEVELS..." next time
.haveFile:     LD   A, #25
                LD   (ZiFi_DbgGamesA), A
                CALL RawPak_Seek0
                CALL RawPak_BuildRunTable      ; build extent (run) table — handles fragmentation
                JP   NC, .ErrRuns              ; table overflow / chain error -> fallback
                SCF
                RET
.ErrRuns:      LD   A, #A7
                JR   .ErrSet
.ErrBpbRead:   LD   A, #A1
                JR   .ErrSet
.ErrBps:       LD   A, #A2
                JR   .ErrSet
.ErrSpc:       LD   A, #A3
                JR   .ErrSet
.ErrPak:       LD   A, #A6                     ; ZUMALVL.PAK (name+size) not found anywhere
.ErrSet:       LD   (ZiFi_DbgGamesA), A
.Err:          OR   A
                RET

; (RawPak_FindInCurrentDir and the fixed /Games/Zuma Deluxe VDAC2/ path were
;  removed — the loader now finds ZUMALVL.PAK only via RawPak_FindPakRecursive,
;  by name + size in any folder. Single code path.)

; ============================================================================
; RawPak_FindPakRecursive — depth-first search of the WHOLE FAT32 tree from the
; root for a file named "ZUMALVL.PAK" whose size == EXPECTED_PAK_SIZE. Lets the
; pack live in any folder (a real card may not use /Games/Zuma Deluxe VDAC2/).
; Out: CF=1 found (RawPak_FileStartClus set); CF=0 not found / SD error.
; Reads EVERY sector of each directory cluster (via AdvanceOne), so it is correct
; for spc>1 cards (the old per-cluster-first-sector scan missed entries at spc=64).
; Result is cached by the caller, so the (potentially slow) walk runs once/session.
; ============================================================================
RawPak_FindPakRecursive:
                LD   HL, RAWPAK_NAME_PAK        ; target = "ZUMALVL.PAK" uppercased
                LD   DE, RawPak_TargetName
.cpyt:         LD   A, (HL)
                CALL RawPak_Upcase
                LD   (DE), A
                INC  HL
                INC  DE
                OR   A
                JR   NZ, .cpyt
                LD   A, 1
                LD   (RawPak_CheckSize), A      ; PAK: совпадение по имени И точному размеру
; RawPak_FindByName — DFS дерева FAT по RawPak_TargetName (+опц. размер). Вызывающий
; заранее ставит RawPak_TargetName (upcase, zero-term) и RawPak_CheckSize (0=только имя,
; 1=имя+EXPECTED_PAK_SIZE). Out: CF=1 найдено (RawPak_FileStartClus + RawPak_FoundSize),
; CF=0 нет/ошибка SD.
RawPak_FindByName:
                XOR  A
                LD   (RawPak_DirStackCnt), A
                LD   (ZiFi_DbgGamesFound), A
                LD   HL, RawPak_RootClus        ; seed the DFS stack with the root dir
                CALL RawPak_DirStackPush
.popLoop:      CALL RawPak_DirStackPop         ; CurClus = next dir; CF=1 if stack empty
                JP   C, .notFound
                LD   A, (ZiFi_DbgGamesFound)    ; diag: count dirs visited
                INC  A
                LD   (ZiFi_DbgGamesFound), A
                XOR  A
                LD   (RawPak_CurSecInClus), A
                LD   (RawPak_HaveLfn), A
.sec:          CALL RawPak_CurrentLbaRegs
                CALL RawPak_ReadSectorBuffer    ; dir sector -> slot 2 #8000, IX=#8000
                JP   C, .notFound               ; SD error -> abort the search
                LD   IX, #8000
.entry:        LD   A, (IX + 0)
                OR   A
                JP   Z, .dirDone                ; 0x00 = end of this directory
                CP   #E5
                JP   Z, .skip                   ; deleted entry (JP: цели сдвинулись > ±128)
                LD   A, (IX + 11)
                CP   #0F
                JP   Z, .lfnFrag                ; LFN fragment -> accumulate
                LD   A, (IX + 11)
                AND  #08
                JR   NZ, .skip                  ; volume label -> skip
                LD   A, (IX + 11)
                AND  #10
                JR   NZ, .isDir                 ; subdirectory
                ; --- regular file: match name, then exact size ---
                LD   A, (RawPak_HaveLfn)
                OR   A
                JR   NZ, .haveName
                CALL RawPak_Build83
.haveName:     CALL RawPak_NameMatch           ; Z if name == RawPak_TargetName
                JR   NZ, .skip
                LD   A, (RawPak_CheckSize)
                OR   A
                JR   Z, .sizeOk                 ; CheckSize=0 → совпадение только по имени
                CALL RawPak_CheckPakSize        ; Z if file size == EXPECTED_PAK_SIZE
                JR   NZ, .skip
.sizeOk:       LD   A, (IX + 26) : LD (RawPak_FileStartClus + 0), A
                LD   A, (IX + 27) : LD (RawPak_FileStartClus + 1), A
                LD   A, (IX + 20) : LD (RawPak_FileStartClus + 2), A
                LD   A, (IX + 21) : LD (RawPak_FileStartClus + 3), A
                LD   A, (IX + 28) : LD (RawPak_FoundSize + 0), A    ; размер файла (байт, LE)
                LD   A, (IX + 29) : LD (RawPak_FoundSize + 1), A
                LD   A, (IX + 30) : LD (RawPak_FoundSize + 2), A
                LD   A, (IX + 31) : LD (RawPak_FoundSize + 3), A
                SCF
                RET
.isDir:        LD   A, (IX + 0)                ; skip "." and ".." (and any dot-leading)
                CP   '.'
                JR   Z, .skip
                LD   A, (IX + 26) : LD (RawPak_Tmp + 0), A
                LD   A, (IX + 27) : LD (RawPak_Tmp + 1), A
                LD   A, (IX + 20) : LD (RawPak_Tmp + 2), A
                LD   A, (IX + 21) : LD (RawPak_Tmp + 3), A
                LD   HL, RawPak_Tmp
                CALL RawPak_DirStackPush        ; push subdir (silently dropped if stack full)
.skip:         XOR  A
                LD   (RawPak_HaveLfn), A
.next:         LD   DE, 32
                ADD  IX, DE
                PUSH IX                          ; sector holds 16 entries (#8000..#81FF)
                POP  HL
                LD   A, H
                CP   #82
                JP   C, .entry                  ; still inside this 512-byte sector
                CALL RawPak_AdvanceOne          ; next dir sector; CF=1 = end of chain
                JP   NC, .sec
                JP   .popLoop                   ; this directory exhausted -> next dir
.dirDone:      JP   .popLoop
.lfnFrag:      CALL RawPak_StoreLfn
                JR   .next
.notFound:     OR   A
                RET

; Try the active Wild Commander panel path stored in page #F7:
;   #F7:#1000 left path, #F7:#2000 right path, #F7:#1FFC active marker.
; Path format is "N:\dir\subdir\#FF". We ignore N for raw SD access, but keep it
; for diagnostics and validate the prefix before walking the FAT tree.
; Out: CF=1 found ZUMALVL.PAK in that directory (RawPak_FileStartClus set).
;      CF=0 if WC path is absent/stale or the file is not there.
RawPak_TryWcPath:
                XOR  A
                LD   (RawPak_ZumaDirValid), A           ; пока папка не подтверждена
                CALL RawPak_ReadWcPath
                RET  NC
                LD   HL, RawPak_RootClus
                LD   DE, RawPak_CurClus
                CALL RawPak_Copy32
                LD   HL, RawPak_WcPathBuf
.nextComp:      LD   A, (HL)
                CP   #FF
                JR   Z, .findPakHere
                CALL RawPak_CopyWcComponent
                RET  NC
                PUSH HL
                LD   A, 1
                LD   (RawPak_FindWantDir), A
                XOR  A
                LD   (RawPak_CheckSize), A
                CALL RawPak_FindEntryInCur
                POP  HL
                RET  NC
                LD   DE, RawPak_CurClus
                PUSH HL
                LD   HL, RawPak_FileStartClus
                CALL RawPak_Copy32
                POP  HL
                JR   .nextComp
.findPakHere:   LD   HL, RawPak_CurClus                  ; CurClus = папка /Games/Zuma Deluxe VDAC2/
                LD   DE, RawPak_ZumaDirClus              ; запомнить её — там же лежат aud/snd паки
                CALL RawPak_Copy32
                LD   A, 1
                LD   (RawPak_ZumaDirValid), A
                CALL RawPak_CopyPakTarget
                XOR  A
                LD   (RawPak_FindWantDir), A
                LD   A, 1
                LD   (RawPak_CheckSize), A
                JP   RawPak_FindEntryInCur

RawPak_ReadWcPath:
                LD   A, WC_RPPG
                SetPage2_A
                LD   HL, #8000 + WC_RAPNL
                LD   A, (HL)
                CP   #41
                JR   Z, .left
                CP   #42
                JR   NZ, .fail
                LD   HL, #8000 + WC_RRPNL
                JR   .havePanel
.left:          LD   HL, #8000 + WC_RLPNL
.havePanel:     LD   A, (HL)
                CP   '0'
                JR   C, .fail
                CP   '9' + 1
                JR   NC, .fail
                LD   (RawPak_WcDevice), A
                INC  HL
                LD   A, (HL)
                CP   ':'
                JR   NZ, .fail
                INC  HL
                LD   A, (HL)
                CP   '\'
                JR   NZ, .fail
                INC  HL
                LD   DE, RawPak_WcPathBuf
                LD   B, WC_PATH_MAX-1
.copy:          LD   A, (HL)
                INC  HL
                CP   #FF
                JR   Z, .term
                LD   (DE), A
                INC  DE
                DJNZ .copy
.fail:          OR   A
                RET
.term:          LD   (DE), A
                SCF
                RET

RawPak_CopyPakTarget:
                LD   HL, RAWPAK_NAME_PAK
                LD   DE, RawPak_TargetName
.cp:            LD   A, (HL)
                CALL RawPak_Upcase
                LD   (DE), A
                INC  HL
                INC  DE
                OR   A
                JR   NZ, .cp
                RET

; HL points at a WC path component. Copy it into RawPak_TargetName, uppercase and
; zero-terminate it. Returns HL at the next component (or #FF), CF=1 on success.
RawPak_CopyWcComponent:
                LD   DE, RawPak_TargetName
                LD   B, 63
.loop:          LD   A, (HL)
                CP   #FF
                JR   Z, .done
                CP   '\'
                JR   Z, .slash
                OR   A
                JR   Z, .fail
                CALL RawPak_Upcase
                LD   (DE), A
                INC  DE
                INC  HL
                DJNZ .loop
.fail:          OR   A
                RET
.slash:         INC  HL
.done:          XOR  A
                LD   (DE), A
                LD   A, D
                CP   RawPak_TargetName >> 8
                JR   NZ, .ok
                LD   A, E
                CP   RawPak_TargetName & #FF
                JR   Z, .fail
.ok:            SCF
                RET

; Search only the directory currently in RawPak_CurClus.
; RawPak_FindWantDir: 1=directory, 0=file. RawPak_CheckSize optionally checks
; EXPECTED_PAK_SIZE for files. Out: CF=1 and FileStartClus/FoundSize set.
RawPak_FindEntryInCur:
                XOR  A
                LD   (RawPak_CurSecInClus), A
                LD   (RawPak_HaveLfn), A
.sec:           CALL RawPak_CurrentLbaRegs
                CALL RawPak_ReadSectorBuffer
                JP   C, .notFound
                LD   IX, #8000
.entry:         LD   A, (IX + 0)
                OR   A
                JP   Z, .notFound
                CP   #E5
                JR   Z, .skip
                LD   A, (IX + 11)
                CP   #0F
                JP   Z, .lfnFrag
                LD   A, (IX + 11)
                AND  #08
                JR   NZ, .skip
                LD   A, (RawPak_FindWantDir)
                OR   A
                JR   NZ, .wantDir
                LD   A, (IX + 11)
                AND  #10
                JR   NZ, .skip
                JR   .match
.wantDir:       LD   A, (IX + 11)
                AND  #10
                JR   Z, .skip
                LD   A, (IX + 0)
                CP   '.'
                JR   Z, .skip
.match:         LD   A, (RawPak_HaveLfn)
                OR   A
                JR   NZ, .haveName
                CALL RawPak_Build83
.haveName:      CALL RawPak_NameMatch
                JR   NZ, .skip
                LD   A, (RawPak_FindWantDir)
                OR   A
                JR   NZ, .save
                LD   A, (RawPak_CheckSize)
                OR   A
                JR   Z, .save
                CALL RawPak_CheckPakSize
                JR   NZ, .skip
.save:          LD   A, (IX + 26) : LD (RawPak_FileStartClus + 0), A
                LD   A, (IX + 27) : LD (RawPak_FileStartClus + 1), A
                LD   A, (IX + 20) : LD (RawPak_FileStartClus + 2), A
                LD   A, (IX + 21) : LD (RawPak_FileStartClus + 3), A
                LD   A, (IX + 28) : LD (RawPak_FoundSize + 0), A
                LD   A, (IX + 29) : LD (RawPak_FoundSize + 1), A
                LD   A, (IX + 30) : LD (RawPak_FoundSize + 2), A
                LD   A, (IX + 31) : LD (RawPak_FoundSize + 3), A
                SCF
                RET
.skip:          XOR  A
                LD   (RawPak_HaveLfn), A
.next:          LD   DE, 32
                ADD  IX, DE
                PUSH IX
                POP  HL
                LD   A, H
                CP   #82
                JP   C, .entry
                CALL RawPak_AdvanceOne
                JP   NC, .sec
.notFound:      OR   A
                RET
.lfnFrag:       CALL RawPak_StoreLfn
                JR   .next

; Z if the dir entry at IX has file size (IX+28..31, LE) == EXPECTED_PAK_SIZE.
RawPak_CheckPakSize:
                LD   A, (IX + 28)
                CP   EXPECTED_PAK_SIZE & #FF
                RET  NZ
                LD   A, (IX + 29)
                CP   (EXPECTED_PAK_SIZE >> 8) & #FF
                RET  NZ
                LD   A, (IX + 30)
                CP   (EXPECTED_PAK_SIZE >> 16) & #FF
                RET  NZ
                LD   A, (IX + 31)
                CP   (EXPECTED_PAK_SIZE >> 24) & #FF
                RET

; DFS directory-cluster stack (LIFO). Entries are 4-byte start clusters.
RawPak_DirStackPush:                            ; HL -> 4-byte cluster; ignored when full
                LD   A, (RawPak_DirStackCnt)
                CP   RAWPAK_DIRSTACK_MAX
                RET  NC
                PUSH HL
                LD   L, A
                LD   H, 0
                ADD  HL, HL
                ADD  HL, HL                      ; HL = cnt * 4
                LD   DE, RawPak_DirStack
                ADD  HL, DE
                EX   DE, HL                      ; DE = dest slot
                POP  HL                          ; HL = src cluster
                LD   BC, 4
                LDIR
                LD   A, (RawPak_DirStackCnt)
                INC  A
                LD   (RawPak_DirStackCnt), A
                RET

RawPak_DirStackPop:                             ; CurClus = top; CF=1 if empty
                LD   A, (RawPak_DirStackCnt)
                OR   A
                JR   Z, .empty
                DEC  A
                LD   (RawPak_DirStackCnt), A
                LD   L, A
                LD   H, 0
                ADD  HL, HL
                ADD  HL, HL
                LD   DE, RawPak_DirStack
                ADD  HL, DE                      ; HL = src slot
                LD   DE, RawPak_CurClus
                LD   BC, 4
                LDIR
                OR   A                           ; CF=0 (A = new count, never wraps to set CF)
                RET
.empty:        SCF
                RET

; A = uppercase(A) for ASCII a-z; other bytes unchanged.
RawPak_Upcase:  CP   'a'
                RET  C
                CP   'z' + 1
                RET  NC
                SUB  #20
                RET

; Build 8.3 effective name from short entry (IX) into RawPak_EntName,
; uppercased, zero-terminated. "NAME.EXT" (dot+ext only if extension present).
RawPak_Build83:
                LD   DE, RawPak_EntName
                PUSH IX
                POP  HL                        ; HL = name field (entry+0)
                LD   B, 8
                CALL .copytrim
                PUSH IX
                POP  HL
                LD   BC, 8
                ADD  HL, BC                    ; HL = ext field (entry+8)
                LD   A, (HL)
                CP   ' '
                JR   Z, .noext
                LD   A, '.'
                LD   (DE), A
                INC  DE
                LD   B, 3
                CALL .copytrim
.noext:        XOR  A
                LD   (DE), A
                RET
.copytrim:     LD   A, (HL)                    ; copy <=B chars, stop at first space
                CP   ' '
                RET  Z
                CALL RawPak_Upcase
                LD   (DE), A
                INC  DE
                INC  HL
                DJNZ .copytrim
                RET

; Compare RawPak_EntName vs RawPak_TargetName (both uppercase, zero-term).
; Out: Z if equal, NZ otherwise.
RawPak_NameMatch:
                LD   HL, RawPak_EntName
                LD   DE, RawPak_TargetName
.nm:           LD   A, (DE)
                CP   (HL)
                RET  NZ
                OR   A
                RET  Z
                INC  HL
                INC  DE
                JR   .nm

; Store one LFN fragment (IX = LFN entry) into RawPak_EntName at (seq-1)*13.
; Sets RawPak_HaveLfn. Chars taken as UTF-16 low byte, uppercased.
RawPak_StoreLfn:
                LD   A, (IX + 0)
                AND  #1F
                DEC  A                          ; idx (0-based)
                LD   HL, RawPak_EntName
                OR   A
                JR   Z, .pos
                LD   DE, 13
.mul:          ADD  HL, DE
                DEC  A
                JR   NZ, .mul
.pos:          LD   A, (IX + 1)  : CALL .put
                LD   A, (IX + 3)  : CALL .put
                LD   A, (IX + 5)  : CALL .put
                LD   A, (IX + 7)  : CALL .put
                LD   A, (IX + 9)  : CALL .put
                LD   A, (IX + 14) : CALL .put
                LD   A, (IX + 16) : CALL .put
                LD   A, (IX + 18) : CALL .put
                LD   A, (IX + 20) : CALL .put
                LD   A, (IX + 22) : CALL .put
                LD   A, (IX + 24) : CALL .put
                LD   A, (IX + 28) : CALL .put
                LD   A, (IX + 30) : CALL .put
                LD   A, 1
                LD   (RawPak_HaveLfn), A
                RET
.put:          CALL RawPak_Upcase
                LD   (HL), A
                INC  HL
                RET

RawPak_Seek0:
                LD   HL, RawPak_FileStartClus
                LD   DE, RawPak_CurClus
                CALL RawPak_Copy32
                XOR  A
                LD   (RawPak_CurSecInClus), A
                RET

RawPak_SkipB:
                LD   A, B
                OR   A
                RET  Z
.loop:         CALL RawPak_AdvanceOne
                RET  C
                DJNZ .loop
                RET

; In: C=dest page, HL=dest offset inside page, B=sector count.
RawPak_ReadSectors:
                LD   A, B
                OR   A
                RET  Z
                LD   A, C
                LD   (RawPak_DstPage), A
                LD   (RawPak_DstOff), HL
                LD   A, B
                LD   (RawPak_ReadCount), A
.loop:         LD   A, (RawPak_DstPage)
                SetPage2_A
                LD   HL, (RawPak_DstOff)
                LD   DE, #8000
                ADD  HL, DE
                PUSH HL
                POP  IX
                CALL RawPak_CurrentLbaRegs
                CALL sd_read_sector
                JR   C, .Err
                CALL RawPak_AdvanceOne
                PUSH AF
                LD   HL, (RawPak_DstOff)
                LD   DE, #0200
                ADD  HL, DE
                BIT  6, H
                JR   Z, .dstSamePage
                RES  6, H
                LD   A, (RawPak_DstPage)
                INC  A
                LD   (RawPak_DstPage), A
.dstSamePage:
                LD   (RawPak_DstOff), HL
                LD   A, (RawPak_ReadCount)
                DEC  A
                LD   (RawPak_ReadCount), A
                POP  AF
                JR   Z, .Ok
                JR   C, .Err
                JR   .loop
.Ok:           OR   A
                RET
.Err:          SCF
                RET

RawPak_AdvanceOne:
                ; RawPak_FatNext clobbers BC (its 7x shift loop uses B; the entry
                ; copy/LDIR use BC). Preserve BC so callers that keep a DJNZ counter
                ; in B across the advance (RawPak_SkipB) are not corrupted — that bug
                ; made LoadNon(1) walk the whole chain to EOC, so the TOC sector was
                ; never read and ZiFi_LevelTOC came back all zeros.
                PUSH BC
                LD   A, (RawPak_CurSecInClus)
                INC  A
                LD   E, A
                LD   A, (RawPak_Spc)
                CP   E
                JR   Z, .nextCluster
                JR   C, .nextCluster
                LD   A, E
                LD   (RawPak_CurSecInClus), A
                POP  BC
                OR   A
                RET
.nextCluster:  XOR  A
                LD   (RawPak_CurSecInClus), A
                CALL RawPak_FatNext
                POP  BC
                RET

RawPak_FatNext:
                LD   HL, RawPak_CurClus
                LD   DE, RawPak_Tmp
                CALL RawPak_Copy32
                LD   B, 7
.shr:          CALL RawPak_ShrTmp1
                DJNZ .shr
                LD   HL, RawPak_FatStart
                LD   DE, RawPak_Tmp
                CALL RawPak_Add32               ; Tmp = FAT sector LBA = (cluster>>7) + FatStart
                ; --- FAT-sector cache: skip the SD read if FatBuf already holds it.
                ; Crucial for RawPak_BuildRunTable: a contiguous chain shares one FAT
                ; sector across 128 clusters, so the full-chain walk drops from N SD
                ; reads to ~N/128 (the FAT is read-only, so the cache never goes stale).
                LD   HL, RawPak_Tmp
                LD   DE, RawPak_FatBufLba
                CALL RawPak_Cmp32
                JR   Z, .fatCached
                LD   HL, (RawPak_Tmp)
                LD   DE, (RawPak_Tmp + 2)
                CALL RawPak_ReadFatSector       ; FAT -> own buffer (NOT slot-2 data buf)
                JR   C, .Err
                LD   HL, RawPak_Tmp             ; remember which FAT sector is buffered
                LD   DE, RawPak_FatBufLba
                CALL RawPak_Copy32
.fatCached:    LD   A, (RawPak_CurClus)
                AND  127
                LD   L, A
                LD   H, 0
                ADD  HL, HL
                ADD  HL, HL
                LD   DE, RawPak_FatBuf
                ADD  HL, DE
                LD   DE, RawPak_CurClus
                LD   BC, 4
                LDIR
                LD   A, (RawPak_CurClus + 3)
                AND  #0F
                LD   (RawPak_CurClus + 3), A
                ; EOC if >= 0x0FFFFFF8.
                CP   #0F
                JR   NZ, .notEoc
                LD   A, (RawPak_CurClus + 2)
                CP   #FF
                JR   NZ, .notEoc
                LD   A, (RawPak_CurClus + 1)
                CP   #FF
                JR   NZ, .notEoc
                LD   A, (RawPak_CurClus + 0)
                CP   #F8
                JR   C, .notEoc
                SCF
                RET
.notEoc:       LD   A, (RawPak_CurClus + 3)
                OR   A
                JR   NZ, .Ok
                LD   A, (RawPak_CurClus + 2)
                OR   A
                JR   NZ, .Ok
                LD   A, (RawPak_CurClus + 1)
                OR   A
                JR   NZ, .Ok
                LD   A, (RawPak_CurClus + 0)
                CP   2
                JR   NC, .Ok
.Err:          SCF
                RET
.Ok:           OR   A
                RET

RawPak_CurrentLbaRegs:
                LD   HL, RawPak_CurClus
                LD   DE, RawPak_Tmp
                CALL RawPak_Copy32
                ; tmp = cluster - 2
                LD   HL, (RawPak_Tmp)
                LD   DE, 2
                OR   A
                SBC  HL, DE
                LD   (RawPak_Tmp), HL
                JR   NC, .noBorrow
                LD   HL, (RawPak_Tmp + 2)
                DEC  HL
                LD   (RawPak_Tmp + 2), HL
.noBorrow:     LD   HL, RawPak_DataStart
                ; tmp = (cluster - 2) * sectors_per_cluster + CurSecInClus.
                ; FAT32 SPC is power-of-two, so multiply by shifting.
                LD   A, (RawPak_Spc)
.spcShift:     CP   1
                JR   Z, .spcDone
                SRL  A
                PUSH AF
                LD   HL, (RawPak_Tmp)
                ADD  HL, HL
                LD   (RawPak_Tmp), HL
                LD   HL, (RawPak_Tmp + 2)
                ADC  HL, HL
                LD   (RawPak_Tmp + 2), HL
                POP  AF
                JR   .spcShift
.spcDone:      LD   A, (RawPak_CurSecInClus)
                LD   E, A
                LD   D, 0
                LD   HL, (RawPak_Tmp)
                ADD  HL, DE
                LD   (RawPak_Tmp), HL
                JR   NC, .addDataStart
                LD   HL, (RawPak_Tmp + 2)
                INC  HL
                LD   (RawPak_Tmp + 2), HL
.addDataStart: LD   HL, RawPak_DataStart
                LD   DE, RawPak_Tmp
                CALL RawPak_Add32
                LD   HL, (RawPak_Tmp)
                LD   DE, (RawPak_Tmp + 2)
                RET

RawPak_ReadSectorBuffer:
                LD   A, ZIFI_BUFFER_PAGE
                SetPage2_A
                LD   IX, #8000
                JP   sd_read_sector

; ============================================================================
; OVL_ProbeBoot (Этап 1 загрузчика Quit) — найти BOOT.$C в корне SD, прочитать
; 1-й сектор, сложить результат в РЕЗИДЕНТНЫЕ Core-переменные для проверки по F12-
; дампу. НИЧЕГО не запускает (это де-риск проба). Вызывается через резидентный
; трамплин ProbeBootHobeta (slot3 = LOADER_OVL_PAGE уже выставлен).
;   Boot_Found = 1 если найден и прочитан, иначе 0.
;   Boot_Clus  = стартовый кластер (4 байта), Boot_Size = размер в байтах (4),
;   Boot_Hdr   = первые 32 байта файла (HOBETA-заголовок).
; ============================================================================
OVL_ProbeBoot:
                XOR  A
                LD   (Boot_Found), A
                CALL ZiFi_Init                  ; SD init (always CF=1)
                CALL RawPak_OpenRoot            ; разобрать BPB/MBR (+найти PAK, игнор)
                JP   NC, .pbDone                ; SD/BPB ошибка → Boot_Found=0
                LD   HL, RAWPAK_NAME_BOOT       ; цель = "BOOT.$C"
                LD   DE, RawPak_TargetName
.pbCpy:         LD   A, (HL)
                CALL RawPak_Upcase
                LD   (DE), A
                INC  HL : INC DE
                OR   A
                JR   NZ, .pbCpy
                XOR  A
                LD   (RawPak_CheckSize), A      ; сначала найти по имени, затем проверить размер BOOT
                CALL RawPak_FindByName          ; CF=1 найден → FileStartClus + FoundSize
                JP   NC, .pbDone
                LD   HL, (RawPak_FoundSize + 0)
                LD   DE, RAWPAK_BOOT_SIZE
                OR   A
                SBC  HL, DE
                JP   NZ, .pbDone
                LD   HL, (RawPak_FoundSize + 2)
                LD   DE, 0
                OR   A
                SBC  HL, DE
                JP   NZ, .pbDone
                LD   HL, RawPak_FileStartClus
                LD   DE, Boot_Clus
                CALL RawPak_Copy32
                LD   HL, RawPak_FoundSize
                LD   DE, Boot_Size
                CALL RawPak_Copy32
                ; --- прочитать 1-й сектор файла → Boot_Hdr (32 байта) ---
                LD   HL, RawPak_FileStartClus
                LD   DE, RawPak_CurClus
                CALL RawPak_Copy32
                XOR  A
                LD   (RawPak_CurSecInClus), A
                CALL RawPak_CurrentLbaRegs      ; HL:DE = абсолютный LBA первого сектора
                LD   (Boot_StartLba + 0), HL
                LD   (Boot_StartLba + 2), DE
                CALL RawPak_ReadSectorBuffer    ; → #8000 (slot2), IX=#8000 (клобает HL:DE)
                JP   C, .pbDone
                LD   HL, #8000
                LD   DE, Boot_Hdr
                LD   BC, 32
                LDIR
                ; число секторов = ceil(size/512) = (size_low16 + 511) >> 9 (файл < 64К)
                LD   HL, (Boot_Size)
                LD   DE, 511
                ADD  HL, DE
                LD   A, H                       ; (size+511) >> 8
                SRL  A                           ; >> 9
                LD   (Boot_SecCount), A
                LD   A, (sd_blkt)               ; режим адресации SD
                LD   (Boot_Blkt), A
                LD   A, 1
                LD   (Boot_Found), A
.pbDone:        RET

; Read the partition BPB (sector at RawPak_PartLba) into the buffer and check it
; looks like FAT32 (bytes/sector == 512). Used to probe SD block-vs-byte
; addressing. Out: CF=0 valid BPB; CF=1 read error or not a 512-byte-sector BPB.
RawPak_ReadPartBpb:
                LD   HL, (RawPak_PartLba + 0)
                LD   DE, (RawPak_PartLba + 2)
                CALL RawPak_ReadSectorBuffer
                RET  C
                LD   IX, #8000
                LD   A, (IX + 11)
                OR   A
                JR   NZ, .bad
                LD   A, (IX + 12)
                CP   2
                JR   NZ, .bad
                OR   A                          ; CF=0 (valid)
                RET
.bad:          SCF
                RET

; Read a FAT sector into RawPak_FatBuf (resident in Core / slot 1). Keeps the
; FAT-chain walk from clobbering the slot-2 data buffer (#8000) that callers
; fill with directory/file sectors. HL:DE = LBA on entry.
RawPak_ReadFatSector:
                LD   IX, RawPak_FatBuf
                JP   sd_read_sector

; --- Sector-run table (handles both contiguous AND fragmented PAK) ----------
; Requirement: read the PAK whether it is one contiguous run or fragmented.
; The old fast path assumed contiguity (LBA = PakLba + N) — true on a freshly
; built image, but a fragmented file would read the wrong sectors on the upper
; levels. Instead we walk the FAT chain once at open time and record the file as
; a list of extents (runs) {LBA, len_sectors}: a contiguous file collapses to a
; single run, a fragmented one to several. RawPak_ReadOneLogicalIX then maps a
; logical sector to a physical LBA via this table — O(runs), no per-read FAT
; walk, so a section still reads in one SD burst (no FT.WriteMem interleaving).

RAWPAK_RUN_MAX  EQU 128                ; max extents (128 * 6 = 768 B). Overflow -> open fails -> L1 fallback.

; RawPak_BuildRunTable — walk the chain from RawPak_FileStartClus, fill
; RawPak_RunTable / RawPak_RunCount. CurSecInClus must be 0 (set by Seek0).
; Out: CF=1 ok; CF=0 on run-table overflow (heavily fragmented) or SD error.
;
; Scans the FAT *directly*, not via RawPak_FatNext: it keeps a pointer into
; RawPak_FatBuf and reads consecutive 4-byte FAT entries, only recomputing the
; FAT-sector LBA (the expensive cluster>>7) when it crosses a 128-entry sector
; boundary or follows a non-sequential link. A contiguous PAK (the common case)
; then costs ~ one FAT-sector read per 128 clusters and a tight inner loop —
; instead of a full cluster>>7 + 32-bit shift chain per cluster.
RawPak_BuildRunTable:
                LD   HL, RawPak_FileStartClus
                LD   DE, RawPak_CurClus
                CALL RawPak_Copy32
                XOR  A
                LD   (RawPak_CurSecInClus), A
                ; --- first run: LBA = LBA(FileStartClus), len starts at 0 ---
                LD   IX, RawPak_RunTable
                CALL RawPak_CurrentLbaRegs      ; HL = LBA lo16, DE = LBA hi16
                LD   (IX + 0), L
                LD   (IX + 1), H
                LD   (IX + 2), E
                LD   (IX + 3), D
                LD   (IX + 4), 0
                LD   (IX + 5), 0
                LD   A, 1
                LD   (RawPak_RunCount), A
                PUSH IX                          ; FatEntryPtr->ReadFatSector clobbers IX
                CALL RawPak_FatEntryPtr          ; HL -> FAT[CurClus] in FatBuf
                POP  IX
                JP   C, .fail
                LD   (RawPak_FatPtr), HL
.enter:        ; CurClus belongs to the current run -> count it
                LD   A, (RawPak_Spc)
                ADD  A, (IX + 4)
                LD   (IX + 4), A
                JR   NC, .noLenCy
                INC  (IX + 5)
.noLenCy:      ; NextClus = masked FAT entry at FatPtr; advance FatPtr by 4
                LD   HL, (RawPak_FatPtr)
                LD   A, (HL) : LD (RawPak_NextClus + 0), A : INC HL
                LD   A, (HL) : LD (RawPak_NextClus + 1), A : INC HL
                LD   A, (HL) : LD (RawPak_NextClus + 2), A : INC HL
                LD   A, (HL) : AND #0F : LD (RawPak_NextClus + 3), A : INC HL
                LD   (RawPak_FatPtr), HL
                ; end of chain? NextClus >= 0x0FFFFFF8
                LD   A, (RawPak_NextClus + 3) : CP #0F : JR NZ, .notEoc
                LD   A, (RawPak_NextClus + 2) : CP #FF : JR NZ, .notEoc
                LD   A, (RawPak_NextClus + 1) : CP #FF : JR NZ, .notEoc
                LD   A, (RawPak_NextClus + 0) : CP #F8 : JP NC, .done
.notEoc:       ; adjacent if NextClus == CurClus + 1
                LD   HL, RawPak_CurClus
                LD   DE, RawPak_Tmp
                CALL RawPak_Copy32
                CALL RawPak_IncTmp32             ; Tmp = CurClus + 1
                LD   HL, RawPak_Tmp
                LD   DE, RawPak_NextClus
                CALL RawPak_Cmp32
                JR   NZ, .jump
                ; --- sequential: CurClus = NextClus, extend the same run ---
                LD   HL, RawPak_NextClus
                LD   DE, RawPak_CurClus
                CALL RawPak_Copy32
                LD   A, (RawPak_CurClus)         ; crossed a FAT-sector boundary?
                AND  127
                JP   NZ, .enter                  ; no -> FatPtr already points to the entry
                PUSH IX                          ; yes -> reload FatPtr for the new FAT sector
                CALL RawPak_FatEntryPtr
                POP  IX
                JP   C, .fail
                LD   (RawPak_FatPtr), HL
                JP   .enter
.jump:         ; --- fragmentation: close current run, open a new one at NextClus ---
                LD   A, (RawPak_RunCount)
                CP   RAWPAK_RUN_MAX
                JP   NC, .fail                   ; too fragmented -> open fails -> L1 fallback
                INC  A
                LD   (RawPak_RunCount), A
                LD   DE, 6
                ADD  IX, DE
                LD   HL, RawPak_NextClus
                LD   DE, RawPak_CurClus
                CALL RawPak_Copy32               ; CurClus = NextClus
                CALL RawPak_CurrentLbaRegs       ; LBA of new cluster (CurSecInClus = 0)
                LD   (IX + 0), L
                LD   (IX + 1), H
                LD   (IX + 2), E
                LD   (IX + 3), D
                LD   (IX + 4), 0
                LD   (IX + 5), 0
                PUSH IX
                CALL RawPak_FatEntryPtr
                POP  IX
                JP   C, .fail
                LD   (RawPak_FatPtr), HL
                JP   .enter
.done:         SCF
                RET
.fail:         OR   A
                RET

; RawPak_FatEntryPtr — ensure the FAT sector holding FAT[CurClus] is in FatBuf
; (read it if the cache misses), and return HL = &FatBuf[(CurClus & 127) * 4].
; Out: CF=0 ok / HL set; CF=1 on SD error. Clobbers AF,BC,DE,HL (and IX via read).
RawPak_FatEntryPtr:
                LD   HL, RawPak_CurClus
                LD   DE, RawPak_Tmp
                CALL RawPak_Copy32
                LD   B, 7
.shr:          CALL RawPak_ShrTmp1
                DJNZ .shr
                LD   HL, RawPak_FatStart
                LD   DE, RawPak_Tmp
                CALL RawPak_Add32               ; Tmp = (CurClus>>7) + FatStart = FAT sector LBA
                LD   HL, RawPak_Tmp
                LD   DE, RawPak_FatBufLba
                CALL RawPak_Cmp32
                JR   Z, .have                    ; already buffered
                LD   HL, (RawPak_Tmp)
                LD   DE, (RawPak_Tmp + 2)
                CALL RawPak_ReadFatSector
                RET  C
                LD   HL, RawPak_Tmp
                LD   DE, RawPak_FatBufLba
                CALL RawPak_Copy32
.have:         LD   A, (RawPak_CurClus)          ; entry offset = (CurClus & 127) * 4
                AND  127
                LD   L, A
                LD   H, 0
                ADD  HL, HL
                ADD  HL, HL
                LD   DE, RawPak_FatBuf
                ADD  HL, DE
                OR   A                            ; CF = 0
                RET

; Read file logical sector (RawPak_LogCur) into (IX). Advances RawPak_LogCur.
; Translates logical sector -> physical LBA through the run table.
; IX (dest) is preserved across the table walk and across sd_read_sector.
RawPak_ReadOneLogicalIX:
                PUSH IX                          ; save dest (we reuse IX to walk the table)
                LD   DE, (RawPak_LogCur)          ; DE = N (logical sector within file)
                LD   IX, RawPak_RunTable
                LD   A, (RawPak_RunCount)
                LD   B, A
.scan:         LD   L, (IX + 4)
                LD   H, (IX + 5)                  ; HL = run.len (sectors)
                LD   A, D                         ; compare N (DE) < len (HL) ?
                CP   H
                JR   C, .found
                JR   NZ, .sub                     ; N hi > len hi -> N >= len
                LD   A, E
                CP   L
                JR   C, .found
.sub:          LD   A, E                          ; N -= len, then next run
                SUB  L
                LD   E, A
                LD   A, D
                SBC  A, H
                LD   D, A
                PUSH DE
                LD   DE, 6
                ADD  IX, DE
                POP  DE
                DJNZ .scan
                POP  IX                            ; logical sector past EOF (bad TOC) -> error
                SCF
                RET
.found:        LD   L, (IX + 0)                   ; HL = run.LBA lo16
                LD   H, (IX + 1)
                ADD  HL, DE                        ; + N (offset within run)
                EX   DE, HL                        ; DE = LBA lo16 result
                LD   L, (IX + 2)                   ; HL = run.LBA hi16
                LD   H, (IX + 3)
                JR   NC, .noCy
                INC  HL                            ; propagate carry into hi16
.noCy:         EX   DE, HL                        ; HL = LBA lo16, DE = LBA hi16
                POP  IX                            ; restore dest
                CALL sd_read_sector                ; reads [HL:DE] -> (IX); CF = SD error
                LD   HL, (RawPak_LogCur)
                INC  HL
                LD   (RawPak_LogCur), HL
                RET

; Tmp = Tmp + 1 (32-bit LE, carry-propagated).
RawPak_IncTmp32:
                LD   HL, RawPak_Tmp
                INC  (HL)
                RET  NZ
                INC  HL : INC (HL)
                RET  NZ
                INC  HL : INC (HL)
                RET  NZ
                INC  HL : INC (HL)
                RET

; Compare 4-byte LE integers at HL and DE. Out: Z if equal. Clobbers AF,B,HL,DE.
RawPak_Cmp32:
                LD   B, 4
.c:            LD   A, (DE)
                CP   (HL)
                RET  NZ
                INC  HL
                INC  DE
                DJNZ .c
                RET

; (VDC_ReadSampleAtHL moved to loader_resident.asm — it runs per-frame during
;  gameplay when this overlay page is NOT mapped, so it must stay resident.)

; Copy 4 bytes: HL=src, DE=dst.
RawPak_Copy32:
                PUSH BC
                LD   BC, 4
                LDIR
                POP  BC
                RET

; Add 4-byte little-endian integer at HL into integer at DE.
RawPak_Add32:
                PUSH BC
                LD   B, 4
                OR   A
.a:            LD   A, (DE)
                ADC  A, (HL)
                LD   (DE), A
                INC  HL
                INC  DE
                DJNZ .a
                POP  BC
                RET

RawPak_ShrTmp1:
                LD   HL, RawPak_Tmp + 3
                SRL  (HL)
                DEC  HL
                RR   (HL)
                DEC  HL
                RR   (HL)
                DEC  HL
                RR   (HL)
                RET


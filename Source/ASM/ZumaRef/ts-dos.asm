                JR   C, .tocOk
                LD   A, #FD
                LD   (ZiFi_GpDbgStep), A
                JP   .Err
.tocOk:         LD   A, 3
                LD   (ZiFi_GpDbgStep), A
                LD   HL, (ZiFi_LevelTOC + 0)
                LD   (ZiFi_GpDbgBgOff), HL
                LD   HL, (ZiFi_LevelTOC + 2)
                LD   (ZiFi_GpDbgBgSize), HL

                ; --- validate the sections we know how to stage -------------
                ; bg must be exactly 256 sectors (8 x 16K pages #07..#0E).
                LD   HL, (ZiFi_LevelTOC + 2)
                LD   A, H : CP 1 : JP NZ, .Err
                LD   A, L : OR A : JP NZ, .Err
                ; track must fit two 16K pages (<= 64 sectors): chunkA #06 + chunkB #0F.
                LD   HL, (ZiFi_LevelTOC + 10)
                LD   A, H : OR A : JP NZ, .Err
                LD   A, L : CP 65 : JP NC, .Err

                ; ===== SD PHASE: read everything into RAM, NO FT812 access. ==
                ; (FT812 and the SD card share the Z-Controller SPI bus #57/#77;
                ;  interleaving SD reads with FT.WriteMem crashed the bus. So we
                ;  do all SD reads first, then all FT uploads — one transition.)
                ; bg: 256 sectors -> staging pages #07..#0E (16K each).
                LD   HL, (ZiFi_LevelTOC + 0)            ; bg_off (file logical)
                LD   (RawPak_LogCur), HL
                LD   A, #07
                LD   (RawPak_StgPage), A
                LD   B, 8
.sdBgPage:      PUSH BC
                LD   A, (RawPak_StgPage)
                SetPage2_A
                LD   IX, #8000
                LD   B, 32
.sdBgSec:       PUSH BC
                CALL RawPak_ReadOneLogicalIX
                LD   DE, 512
                ADD  IX, DE
                POP  BC
                DJNZ .sdBgSec
                LD   A, (RawPak_StgPage)
                INC  A
                LD   (RawPak_StgPage), A
                POP  BC
                DJNZ .sdBgPage
                LD   A, #34
                LD   (ZiFi_GpDbgStep), A

                ; palette: 1 sector -> staging page #03.
                LD   HL, (ZiFi_LevelTOC + 4)            ; pal_off
                LD   (RawPak_LogCur), HL
                LD   A, ZIFI_BUFFER_PAGE
                SetPage2_A
                LD   IX, #8000
                CALL RawPak_ReadOneLogicalIX

                ; track: chunkA (<=32 sec) -> page #06; chunkB (rest) -> page #0F.
                ; PAK pads chunkA to a full 16K page for split tracks, so logical
                ; sectors track_off..+31 = chunkA and +32.. = chunkB (contiguous in
                ; RawPak_LogCur). total recomputed from TOC (SetPage2_A clobbers BC).
                LD   HL, (ZiFi_LevelTOC + 8)            ; track_off (file logical)
                LD   (RawPak_LogCur), HL
                ; --- chunkA -> #06 ---
                LD   A, TRACK_L01_PAGE
                SetPage2_A
                LD   IX, #8000
                LD   A, (ZiFi_LevelTOC + 10)            ; total track sectors
                CP   33
                JR   C, .trkAcnt                        ; total<=32 -> chunkA = total
                LD   A, 32                              ; cap chunkA at one page
.trkAcnt:       LD   B, A
.sdTrkA:        PUSH BC
                CALL RawPak_ReadOneLogicalIX
                LD   DE, 512
                ADD  IX, DE
                POP  BC
                DJNZ .sdTrkA
                ; --- chunkB -> #0F (only if total > 32) ---
                LD   A, (ZiFi_LevelTOC + 10)
                CP   33
                JR   C, .sdTrkDone                      ; one page only
                SUB  32                                 ; remaining sectors
                LD   B, A
                LD   A, TRACK_PAGE2
                SetPage2_A
                LD   IX, #8000
.sdTrkB:        PUSH BC
                CALL RawPak_ReadOneLogicalIX
                LD   DE, 512
                ADD  IX, DE
                POP  BC
                DJNZ .sdTrkB
.sdTrkDone:
                LD   A, #35
                LD   (ZiFi_GpDbgStep), A

                ; ===== FT PHASE: upload from RAM, NO SD access. =============
                ; bg: pages #07..#0E -> BG_RAMG (16K each), same as the fallback.
                LD   HL, BG_RAMG_ADDR & #FFFF
                LD   (BgRamL), HL
                LD   A, (BG_RAMG_ADDR >> 16) & #FF
                LD   (BgRamH), A
                LD   A, #07
                LD   (RawPak_StgPage), A
                LD   B, 8
.ftBgPage:      PUSH BC
                LD   A, (RawPak_StgPage)
                SetPage2_A
                LD   HL, #8000
                LD   BC, 16384
                LD   A, (BgRamH)
                LD   DE, (BgRamL)
                CALL FT.WriteMem
                LD   HL, (BgRamL)
                LD   DE, #4000
                ADD  HL, DE
                LD   (BgRamL), HL
                JR   NC, .ftNoCy
                LD   A, (BgRamH)
                INC  A
                LD   (BgRamH), A
.ftNoCy:        LD   A, (RawPak_StgPage)
                INC  A
                LD   (RawPak_StgPage), A
                POP  BC
                DJNZ .ftBgPage
                LD   A, 4
                LD   (ZiFi_GpDbgStep), A

                ; palette: page #03 -> BG_PALETTE_RAMG (512 bytes).
                LD   A, ZIFI_BUFFER_PAGE
                SetPage2_A
                LD   HL, #8000
                LD   BC, 512
                LD   A, (BG_PALETTE_RAMG >> 16) & #FF
                LD   DE, BG_PALETTE_RAMG & #FFFF
                CALL FT.WriteMem
                LD   A, 6
                LD   (ZiFi_GpDbgStep), A

                ; bg/palette uploaded to RAM_G, track staged in page #06.
                SCF
.Err:           PUSH AF
                CALL ZiFi_Done
                POP  AF
                RET
.InitErr:       CALL ZiFi_Done
                OR   A
                RET

; LoadLevelFromPack — open ZUMALVL.PAK and load CurrentLevel's TOC entry
; into ZiFi_LevelTOC.
;   Out: CF=1 ok, CF=0 error
LoadLevelFromPack_Setup:
                CALL ZiFi_Init
                JR   NC, .InitErr
                CALL ZiFi_PakOpen
                JR   NC, .Err
                LD   A, (CurrentLevel)
                CALL ZiFi_PakReadToc
.Err:           PUSH AF
                CALL ZiFi_Done
                POP  AF
                RET
.InitErr:       CALL ZiFi_Done
                OR   A
                RET

; LoadLevelFromPack — original dead-code entry point for incremental verification.
; Doesn't yet stream assets to FT812 — only opens pack and reads TOC entry,
; so we can confirm ZiFi flow works without breaking compiled-slot path.
;   In : A = level index (0..21)
;   Out: CF=1 ok, ZiFi_LevelTOC filled
LoadLevelFromPack:
                PUSH AF
                CALL ZiFi_Init
                JR   NC, .InitErr
                POP  AF
                PUSH AF
                CALL ZiFi_PakOpen
                JR   NC, .Err
                POP  AF
                CALL ZiFi_PakReadToc
                PUSH AF
                CALL ZiFi_Done
                POP  AF
                RET
.Err:           POP  AF
                PUSH AF
                CALL ZiFi_Done
                POP  AF
                OR   A                   ; CF=0
                RET
.InitErr:       POP  AF
                CALL ZiFi_Done
                OR   A
                RET

; OVL_LoadLevelSelectPreviewAssets — stream the selected level's preview
; thumbnail (raw ARGB4, 306x196 padded to 8x16K pages) from ZUMALVL.PAK into
; LS_PREVIEW_BG_RAMG. Entered via the resident LoadLevelSelectPreviewAssets
; trampoline (slot 3 holds LOADER_OVL_PAGE; the trampoline restores PAGE3=#04 and
; manages interrupts, so this routine must NOT touch slot 3). SD and FT phases
; stay separate (FT812 + SD share the Z-Controller SPI bus). Out: CF=1 ok / CF=0.
OVL_LoadLevelSelectPreviewAssets:
                CALL ZiFi_Init
                JP   NC, .err_init
                CALL ZiFi_PakOpen
                JP   NC, .err
                LD   A, (CurrentLevel)
                CP   LEVEL_RUNTIME_COUNT
                JR   C, .idx_ok
                XOR  A
.idx_ok:        CALL ZiFi_PakReadToc
                JP   NC, .err
                ; Require exactly the canonical raw thumbnail size.
                LD   HL, (ZiFi_LevelTOC + 18)           ; preview_size
                LD   A, H
                CP   1
                JP   NZ, .err
                LD   A, L
                OR   A                                  ; 8 pages * 32 sectors = #0100
                JP   NZ, .err
                ; SD phase: preview_off -> pages #07..#0E.
                LD   HL, (ZiFi_LevelTOC + 16)           ; preview_off
                LD   (RawPak_LogCur), HL
                LD   A, #07
                LD   (RawPak_StgPage), A
                LD   B, 8
.sd_page:       PUSH BC
                LD   A, (RawPak_StgPage)
                SetPage2_A
                LD   IX, #8000
                LD   B, 32
.sd_sec:        PUSH BC
                CALL RawPak_ReadOneLogicalIX
                JR   C, .sd_read_err
                LD   DE, 512
                ADD  IX, DE
                POP  BC
                DJNZ .sd_sec
                LD   A, (RawPak_StgPage)
                INC  A
                LD   (RawPak_StgPage), A
                POP  BC
                DJNZ .sd_page
                ; FT phase: pages #07..#0E -> LS_PREVIEW_BG_RAMG.
                LD   HL, LS_PREVIEW_BG_RAMG & #FFFF
                LD   (BgRamL), HL
                LD   A, (LS_PREVIEW_BG_RAMG >> 16) & #FF
                LD   (BgRamH), A
                LD   A, #07
                LD   (RawPak_StgPage), A
                LD   B, 8
.ft_page:       PUSH BC
                LD   A, (RawPak_StgPage)
                SetPage2_A
                LD   HL, #8000
                LD   BC, 16384
                LD   A, (BgRamH)
                LD   DE, (BgRamL)
                CALL FT.WriteMem
                LD   HL, (BgRamL)
                LD   DE, #4000
                ADD  HL, DE
                LD   (BgRamL), HL
                JR   NC, .ft_no_carry
                LD   A, (BgRamH)
                INC  A
                LD   (BgRamH), A
.ft_no_carry:   LD   A, (RawPak_StgPage)
                INC  A
                LD   (RawPak_StgPage), A
                POP  BC
                DJNZ .ft_page
                CALL SetCurrentTrackPage
                CALL ZiFi_Done
                SCF
                RET
.sd_read_err:   POP  BC
                POP  BC
.err:           CALL ZiFi_Done
.err_init:      CALL SetCurrentTrackPage
                OR   A
                RET

; Отправка байта данных в GS. КАК В WC (gsplayer GSLOAD/TE0): СНАЧАЛА ждём, пока
; в FIFO освободится место (bit7 статуса #BB сброшен), ПОТОМ пишем байт в #B3.
; Раньше было наоборот (писали, потом ждали) — на эмуляторе прокатывало, но на
; реальном MultiSound запись без проверки места переполняет FIFO → байты теряются
; → стрим модуля бьётся и загрузка не завершается. Байт сохраняем в E на время ожидания.
GS_SendDataOverlay:
                PUSH HL
                PUSH DE
                LD   E, A                       ; сохранить байт данных
                LD   BC, GS_PORT_CMD            ; #BB — статус
                LD   HL, GS_WAIT_TIMEOUT
.wdat:          IN   A, (C)
                RLCA                            ; bit7 -> CF (1 = FIFO полон/не готов)
                JR   NC, .ready                 ; bit7=0 -> есть место
                DEC  HL
                LD   A, H
                OR   L
                JR   NZ, .wdat
                POP  DE
                POP  HL
                XOR  A
                LD   (BootBusOwner), A
                OR   A                          ; таймаут -> CF=0
                RET
.ready:         LD   A, E
                LD   BC, GS_PORT_DATA           ; #B3 — данные
                OUT  (C), A                     ; место есть -> пишем
                POP  DE
                POP  HL
                SCF
                RET

GS_ReadDataWaitOverlay:
                LD   BC, GS_PORT_CMD
                LD   HL, GS_WAIT_TIMEOUT
.wait:          IN   A, (C)
                RLCA
                JR   C, .ready
                DEC  HL
                LD   A, H
                OR   L
                JR   NZ, .wait
                OR   A
                RET
.ready:         LD   BC, GS_PORT_DATA
                IN   A, (C)
                SCF
                RET

